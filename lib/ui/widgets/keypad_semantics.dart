/// Localized TalkBack / VoiceOver labels for keypad buttons.
class KeypadSemantics {
  const KeypadSemantics({
    required this.clear,
    required this.toggleSign,
    required this.percent,
    required this.divide,
    required this.multiply,
    required this.subtract,
    required this.add,
    required this.decimal,
    required this.equals,
  });

  final String clear;
  final String toggleSign;
  final String percent;
  final String divide;
  final String multiply;
  final String subtract;
  final String add;
  final String decimal;
  final String equals;
}
